#include "lem_in.h"

int		path_len(char *path)
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (!path)
		return (0);
	while (path[i])
	{
		if (is_space(path[i]))
			j++;
		i++;
	}
	return (j);
}
